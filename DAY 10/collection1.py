player_name=['shanto','Liton','Riad','Mushfiq']
print(player_name)

# append function: TO ADD VALUE IN A LIST
player_name.append('Mustafiz')
print(player_name)

# remove function: TO REMOVE VALUES FROM A LIST
player_name.remove('Mushfiq')
print(player_name)

# reverse function: TO REVERSE THE VALUES IN A LIST
player_name.reverse()
print(player_name)

# pop function: TO REMOVE LAST VALUES IN LIST
player_name.pop()
print(player_name)
